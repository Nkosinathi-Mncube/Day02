#include <unistd.h>

void ft_putchar(char c)
{
	write(1,&c,1);
}

void ft_print_alphabet(void)
{	
	int y;
	y=97;
	
	while(y<=122)
	{
		ft_putchar(y);
		y++;
	}
}
